<script>
  import { onDestroy, onMount } from 'svelte';
  import Icon from 'fa-svelte';
  import { faFrown, faMeh, faSmile } from '@fortawesome/free-regular-svg-icons';
  import {
    Input,
    Label,
    Button,
    Modal,
    ModalBody,
    ModalFooter,
    ModalHeader,
  } from 'sveltestrap';

  import { PlusIcon } from 'svelte-feather-icons';

  import { board, ranks, cards } from './store.js';
  import { updateBoard, createCard, getCards, getBoard } from './api.js';

  import FloatingActionButton from './components/FloatingActionButton.svelte';
  import Rank from './components/Rank.svelte';
  import Header from './components/Header.svelte';
  import CardForm from './components/CardForm.svelte';

  let unsubscribe;

  async function update() {
    const [b, c] = await Promise.all([
      getBoard($board.id),
      getCards($board.id),
    ]);
    board.set(b);
    cards.set(c);
  }

  onMount(async () => {
    await update();
    if ($board.owner) unsubscribe = board.subscribe(b => updateBoard(b));
    setInterval(async () => await update(), 10000);
  });
  onDestroy(() => $board.owner && unsubscribe());

  let selectedRank = $ranks[0].id;
  let showNewCardModal = false;
  let showNewCardForm = false;
  let newCardComment = '';

  const rankDetails = {
    mad: {
      icon: faFrown,
      selected: 'text-danger border-top border-danger',
      deselected: 'text-danger border-top border-light',
      color: 'text-danger border-danger',
    },
    sad: {
      icon: faMeh,
      selected: 'text-primary border-top border-primary',
      deselected: 'text-primary border-top border-light',
      color: 'text-primary border-primary',
    },
    glad: {
      icon: faSmile,
      selected: 'text-success border-top border-success',
      deselected: 'text-success border-top border-light',
      color: 'text-success border-success',
    },
  };

  function toggleNewCardForm() {
    showNewCardForm = !showNewCardForm;
  }

  function toggleNewCardModal() {
    showNewCardModal = !showNewCardModal;
  }

  async function newCard() {
    const tempId = Math.floor(Math.random() * 10000);
    cards.append({
      id: tempId,
      name: 'Card',
      description: newCardComment,
      rank_id: selectedRank,
      uncommitted: true,
      votes: 0,
      created_at: {
        secs_since_epoch: Date.now() / 1000,
      },
    });
    cards.replace(
      tempId,
      await createCard($board.id, selectedRank, newCardComment)
    );
    newCardComment = '';
  }
</script>

<style>
  .scroll {
    overflow: auto;
  }

  .spacer {
    flex: 0 0 1em;
    border-right: 0.1em solid #e6e6e6;
  }

  .add-button {
    position: fixed;
    bottom: 1em;
    right: 1em;
    width: 3em;
    height: 3em;
  }

  :global(.svelte-tabs) {
    flex: 1 1 1em;
    display: flex;
    flex-direction: column;
  }

  :global(.svelte-tabs__tab-list) {
    display: table;
    table-layout: fixed;
    width: 100%;
  }

  :global(.svelte-tabs li.svelte-tabs__tab) {
    display: table-cell;
    text-align: center;
  }

  input[type='radio'] {
    display: none;
    margin: 10px;
  }

  input[type='radio'] + label {
    display: inline-block;
    flex: 1 1;
    margin: -2px;
    padding: 4px 12px;
    text-align: center;
  }

  @media (max-width: 768px) {
    .add-button {
      bottom: 4em;
    }
  }
</style>

<div class="d-flex h-100 flex-column">

  <Header />

  {#if showNewCardForm}
    <div class="flex-grow-0 p-2 d-block d-md-none">
      <Label for="cardText" class="text-primary">New Card</Label>
      <Input
        readonly={undefined}
        id="cardText"
        type="textarea"
        placeholder="We need more snacks..."
        bind:value={newCardComment}
        class="h-75 mb-2" />
      <div class="d-flex justify-content-end">
        <Button class="mx-1" color="secondary" on:click={toggleNewCardForm}>
          Cancel
        </Button>
        <Button
          class="mx-1"
          color="primary"
          on:click={() => {
            toggleNewCardForm();
            newCard();
          }}>
          Create
        </Button>
      </div>
    </div>
  {:else}
    <div class="d-none d-md-flex justify-content-center pt-3 scroll">
      {#each $ranks as rank, i}
        <Rank
          bind:rank
          color={rankDetails[rank.name.toLowerCase()].color}
          icon={rankDetails[rank.name.toLowerCase()].icon} />
        {#if i !== 2}
          <div class="spacer my-5" />
        {/if}
      {:else}
        <p>
          You have no columns!
          <br />
          Add some with the button on the right.
        </p>
      {/each}
    </div>

    <div class="d-block flex-grow-1 d-md-none scroll">
      {#each $ranks as rank}
        {#if rank.id == selectedRank}
          <Rank
            bind:rank
            color={rankDetails[rank.name.toLowerCase()].color}
            icon={rankDetails[rank.name.toLowerCase()].icon} />
        {/if}
      {:else}
        <p>
          You have no columns!
          <br />
          Add some with the button on the right.
        </p>
      {/each}
    </div>
  {/if}

  <div class="d-flex d-md-none border-top w-100 fixed-bottom">
    {#each $ranks as rank}
      <div class="flex-grow-1">
        <input
          readonly={undefined}
          type="radio"
          id={rank.id}
          bind:group={selectedRank}
          value={rank.id} />
        <label
          for={rank.id}
          class="{selectedRank == rank.id ? rankDetails[rank.name.toLowerCase()].selected : rankDetails[rank.name.toLowerCase()].deselected}
          col">
          <div class="icon">
            <Icon icon={rankDetails[rank.name.toLowerCase()].icon} />
          </div>
          {rank.name}
        </label>
      </div>
    {/each}
  </div>

  {#if !showNewCardForm}
    <div
      class="d-sm-block d-md-none add-button {$board.cards_open ? '' : 'invisible'}">
      <FloatingActionButton icon={PlusIcon} on:click={toggleNewCardForm} />
    </div>
  {/if}

  <div
    class="d-none d-md-block add-button {$board.cards_open ? '' : 'invisible'}">
    <FloatingActionButton icon={PlusIcon} on:click={toggleNewCardModal} />
  </div>
</div>

<Modal
  class="d-none d-md-block"
  isOpen={showNewCardModal}
  toggle={toggleNewCardModal}>
  <ModalHeader toggle={toggleNewCardModal}>New Card</ModalHeader>
  <ModalBody>
    <CardForm bind:rankId={selectedRank} bind:comment={newCardComment} />
  </ModalBody>
  <ModalFooter>
    <Button color="secondary" on:click={toggleNewCardModal}>Cancel</Button>
    <Button
      color="primary"
      on:click={() => {
        toggleNewCardModal();
        newCard();
      }}>
      Create
    </Button>
  </ModalFooter>
</Modal>
